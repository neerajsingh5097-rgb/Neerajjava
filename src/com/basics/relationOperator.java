package com.basics;

public class relationOperator {

	public static void main(String[] args) {
		int n = 10, m= 20;
		
//		Relation Operator ==, < , > , <=, >=, !=,  Boolean values (true/false
		
		System.out.println(n == m);
		System.out.println(n > m);
		System.out.println(n < m);
		System.out.println(n <= m);
		System.out.println(n >= m);
		System.out.println(n != m);

	}

}
